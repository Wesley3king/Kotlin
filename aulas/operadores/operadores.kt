fun main() {
    var valor1: Int = 4
    var valor2 = 8
    // operadoers aritméticos
    // + soma
    println(valor1 + valor2)
    // - subtração
    println(valor1 - valor2)
    // * multiplicação
    println(valor1 * valor2)
    // / divisão
    println(valor1 / valor2)
    // % resto de divisão
    println(valor1 % valor2)
    // ++ incremento
    println(++valor1)
    // -- decremento
    println(--valor1)

    // operadores relacionais
    // == igual
    // > maior que
    // < menor que
    // >= maior ou igual
    // <= menor ou igual
    // != diferente

    // operadores lógicos
    // && e
    // || ou
}